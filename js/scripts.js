function reply_click(clicked_id)
{
  let release_version = clicked_id
  var rv = document.getElementsByClassName('release_version');
  var rd = document.getElementsByClassName('release_date');
  var sp = document.getElementsByClassName('sprint');
  var i_v = document.getElementsByClassName('ios_version');
  var i_s = document.getElementsByClassName('ios_startdate');
  var i_e = document.getElementsByClassName('ios_enddate');
  var m_v = document.getElementsByClassName('mac_version');
  var m_s = document.getElementsByClassName('mac_startdate');
  var m_e = document.getElementsByClassName('mac_enddate');
  var s_v = document.getElementsByClassName('server_version');
  var s_s = document.getElementsByClassName('server_startdate');
  var s_e = document.getElementsByClassName('server_enddate');
  var s_1 = document.getElementsByClassName('summary_1');
  var s_2 = document.getElementsByClassName('summary_2');
  var s_3 = document.getElementsByClassName('summary_3');
  var d_a_1 = document.getElementsByClassName('description_added_1');
  var d_r_1 = document.getElementsByClassName('description_revised_1');
  var d_d_1 = document.getElementsByClassName('description_deprecated_1');
  var d_b_1 = document.getElementsByClassName('description_bugfix_1');
  document.getElementById('github_release').href = mydata[release_version]['archive']['github_release'];
  document.getElementById('github_branch').href = mydata[release_version]['archive']['github_branch'];

  var display = function(m, k){
    for (let i = 0; i < m.length; i++) {
        const e = m[i];
        e.innerText = mydata[release_version][k];
    }   
  };
  var display2 = function(m, k1, k2){
    for (let i = 0; i < m.length; i++) {
        const e = m[i];
        e.innerText = mydata[release_version][k1][k2];
    }   
  };
  var display3 = function(m, k1, k2, k3){
    for (let i = 0; i < m.length; i++) {
        const e = m[i];
        e.innerText = mydata[release_version][k1][k2][k3];
    }   
  };

  display(rv, 'release_version')
  display(rd, 'release_date')
  display(sp, 'sprint')
  display2(i_v, 'ios', 'version')
  display2(i_s, 'ios', 'startdate')
  display2(i_e, 'ios', 'enddate')
  display2(m_v, 'mac', 'version')
  display2(m_s, 'mac', 'startdate')
  display2(m_e, 'mac', 'enddate')
  display2(s_v, 'server', 'version')
  display2(s_s, 'server', 'startdate')
  display2(s_e, 'server', 'enddate')
  display2(s_1, 'summary', '1')
  display2(s_2, 'summary', '2')
  display2(s_3, 'summary', '3')
  display3(d_a_1, 'description', 'added', '1')
  display3(d_r_1, 'description', 'revised', '1')
  display3(d_d_1, 'description', 'deprecated', '1')
  display3(d_b_1, 'description', 'bugfix', '1')


  var options = {
    chart: {
      height: "100%",
      type: "rangeBar",
      toolbar: {
        show: false,
      },
    },
    plotOptions: {
      bar: {
        horizontal: true,
        // dataLabels: {
        //   position: 'top',
        // }
      },
    },
    grid: {
      xaxis: {
          lines: {
              show: false
          }
      },   
      yaxis: {
          lines: {
              show: false
          }
      },  
    },
    xaxis: {
      type: "datetime",
      // labels:{
      //   show:false,
      // },
      axisTicks: {
        show: false,
      },
      axisBorder: {
        show: false,
      },
    },
    dataLabels: {
      enabled: true,
      formatter: function (val, opts) {
          return ((val[1]-val[0])/1000/60/60/24).toString() + " days"
      },
      textAnchor: 'middle',
      offsetX: 0,
      offsetY: 0,
      style: {
          fontSize: '15px',
          fontFamily: 'Arial, Helvetica, sans-serif',
          fontWeight: 'bold',
          colors: ["#FAFAFA"],
      },
    },
    tooltip: {
      enabled: false,
    },
    series: [
      {
        name : "version show",
        data: [
          {
            x: "IOS",
            y: [new Date(mydata[release_version]['ios']['startdate']).getTime(),new Date(mydata[release_version]['ios']['enddate']).getTime()],
            fillColor: "#FF4560",
          },
          {
            x: "MAC",
            y: [new Date(mydata[release_version]['mac']['startdate']).getTime(),new Date(mydata[release_version]['mac']['enddate']).getTime()],            fillColor: "#FEB019",
          },
          {
            x: "SERVER",
            y: [new Date(mydata[release_version]['server']['startdate']).getTime(),new Date(mydata[release_version]['server']['enddate']).getTime()],
            fillColor: "#00E396"
          },
        ],
      },
    ]
  };
  var chart = new ApexCharts(document.querySelector("#chart"), options);
  chart.render();
  chart.resetSeries();
}
